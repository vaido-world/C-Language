/*  function name: sumOfNumbers()
    this function will return sum of two numbers*/
 
int sumOfNumbers(int x,int y){
    int sum;
    sum=x+y;
    return sum;
}
 
 
/*  function name: sumOfNumbers()
    this function will return average of two numbers*/
 
float avgOfNumbers(int x,int y){
    int sum;
    float avg;
     
    sum=x+y;
    avg=sum/2;
    return avg;
}